    public void cargarTabla(){
        //Crea una variable para conectar con la logica
        ControlLogica varLogica = new ControlLogica();
        //Crea una tabla
        DefaultTableModel modeloTabla = new DefaultTableModel(){ 
            @Override
            public boolean isCellEditable(int row, int column){
                return false;
            }
        };
        //Insercion de los titulos a las columnas de la tabla
        String titulo[] = {"ID","nombre","Precio","Cantidad"};
        modeloTabla.setColumnIdentifiers(titulo);
        
        //recupera la lista de productos de la base de datos
        List<Producto> listaProductos = varLogica.buscarProductos();
        //Recorre la lista de tareas y las añade a las filas
        if(listaProductos != null){
            for(Producto productos: listaProductos){
                Object[] objeto = {productos.getId(),productos.getNombre(),productos.getPrecioUnit(),productos.getCantidad()};
                modeloTabla.addRow(objeto);
            }
        }
        //Se agrega la lista de tareas a la tabla
        tablaProductos.setModel(modeloTabla);
    }
    
