    private void btnAñadirCarritoActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAñadirCarritoActionPerformed
        //Crea una variable Producto
        Producto producto;
        //Crea una variable para conectar con la logica
        ControlLogica varLogica = new ControlLogica();
        //Obtiene el num de la fila del produto
        int i = tablaProductos.getSelectedRow();
        //Obtiene el num de la columna del produto
        int j = tablaProductos.getSelectedColumn();
        //Obtiene el id del producto
        int id = (int) tablaProductos.getValueAt(i, j);
        //busca el producto en la base de datos
        producto = varLogica.buscarProducto(id);
        //añade el producto al carrito de compra
        listaCompra.add(producto);
        //Quita un producto del stock
        producto.setCantidad(producto.getCantidad()-1);
        varLogica.modificarProducto(producto);
        //suma los precios unitarios de los productos seleccionados
        precioTotal +=  producto.getPrecioUnit();
        //setea el precio total de la compra
        txtPrecioTotal.setText("$"+precioTotal+"");
        
    }//GEN-LAST:event_btnAñadirCarritoActionPerformed

